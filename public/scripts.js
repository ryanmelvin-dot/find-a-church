// ─── Data source ───────────────────────────────────────────────────────────
// The browser fetches data from a secure Netlify serverless function.
// The Airtable token lives ONLY in Netlify's environment variables — it is
// never sent to the browser.
//
// To run locally: install the Netlify CLI (npm i -g netlify-cli)
// then run `netlify dev` instead of opening index.html directly.
//
// Proxy endpoint (defined in netlify/functions/churches.mjs):
// On localhost (netlify dev) we request ?debug=1 so the function includes
// field-name diagnostics in the response.
const DATA_URL = "/.netlify/functions/churches"
  + (["localhost", "127.0.0.1"].includes(location.hostname) ? "?debug=1" : "");

// Field name map — must match the Airtable column names (exactly, or fuzzily
// per normalizeKey below). Both tables join on the "GCFA ID" column.
const CHURCH_FIELDS = {
  gcfa:     "GCFA ID",
  name:     "Account Name",
  district: "District",
  phone:    "Phone",
  email:    "Primary Email",
  street:   "Physical Address (Street)",
  city:     "Physical Address (City)",
  state:    "Physical Address (State/Province)",
  zip:      "Physical Address (ZIP/Postal Code)",
  website:  "Website",
  // Note: the photo/attachment field is auto-detected in normalizeChurches,
  // so no field name constant is needed here.
};

const SERVICE_FIELDS = {
  gcfa:            "GCFA ID",
  serviceName:     "Service Name",
  serviceTimeName: "Service Time Name",
  serviceTime:     "Service Time",
  serviceDay:      "Service Day",
  serviceType:     "Service Type",
};

// Pre-geocoded coordinates, keyed by GCFA ID: { "123456": [lat, lng], ... }
// Generated by tools/geocode-churches.ps1 — rerun it when churches are
// added or addresses change in Airtable.
const GEO_URL = "assets/churches-geo.json";

