// ─── Map view ─────────────────────────────────────────────────────────────
// One Leaflet map of the whole state with every filtered church plotted at
// once (canvas circle markers stay fast with 600+ dots). Coordinates come
// from the pre-generated churches-geo.json; churches without coordinates
// still appear in the sidebar and open the detail modal instead.

const INDIANA_CENTER = [39.85, -86.26];

// Black "you are here" pin shown after the visitor uses Near me
const YOU_PIN_SVG = `
  <svg xmlns="http://www.w3.org/2000/svg" width="28" height="40" viewBox="0 0 28 40">
    <path d="M14 0C6.3 0 0 6.3 0 14c0 10.5 14 26 14 26s14-15.5 14-26C28 6.3 21.7 0 14 0z" fill="#141827"/>
    <circle cx="14" cy="14" r="5.5" fill="#ffffff"/>
  </svg>`;

function renderMapView(churches, container) {
  if (!churches.length) return;

  mapSelectedIndex = -1;

  const layout  = document.createElement("div");
  layout.className = "map-layout";

  const listDiv = document.createElement("div");
  listDiv.className = "map-list";

  const statusBar = document.createElement("div");
  statusBar.className = "map-status";
  listDiv.appendChild(statusBar);

  const mapPane = document.createElement("div");
  mapPane.className = "map-pane";

  layout.appendChild(listDiv);
  layout.appendChild(mapPane);
  container.appendChild(layout);

  // Leaflet failed to load — keep the sidebar usable via the detail modal
  if (typeof L === "undefined") {
    statusBar.textContent = `${churches.length} church${churches.length !== 1 ? "es" : ""}`;
    mapPane.innerHTML = `<div class="map-placeholder"><p>The map could not be loaded.<br>Click a church for details.</p></div>`;
    churches.forEach((church, i) => {
      const item = buildMapSidebarItem(church, i + 1);
      const open = () => openChurchModal(church, item);
      item.addEventListener("click", open);
      item.addEventListener("keydown", (e) => {
        if (e.key !== "Enter" && e.key !== " ") return;
        e.preventDefault();
        open();
      });
      listDiv.appendChild(item);
    });
    return;
  }

  const mapDiv = document.createElement("div");
  mapDiv.className = "leaflet-host";
  mapPane.appendChild(mapDiv);

  leafletMap = L.map(mapDiv, { preferCanvas: true });
  leafletMap.setView(INDIANA_CENTER, 7);
  L.tileLayer("https://tile.openstreetmap.org/{z}/{x}/{y}.png", {
    maxZoom: 19,
    attribution: '&copy; <a href="https://www.openstreetmap.org/copyright" target="_blank" rel="noopener noreferrer">OpenStreetMap</a> contributors',
  }).addTo(leafletMap);

  const items   = [];
  const markers = [];
  let plotted   = 0;

  const highlightItem = (i, scroll) => {
    listDiv.querySelectorAll(".map-item.is-active")
      .forEach((el) => el.classList.remove("is-active"));
    items[i].classList.add("is-active");
    if (scroll) items[i].scrollIntoView({ behavior: "smooth", block: "nearest" });
    mapSelectedIndex = i;
  };

  churches.forEach((church, i) => {
    const item   = buildMapSidebarItem(church, i + 1);
    const coords = churchGeo[church.gcfa];
    let marker   = null;

    if (coords) {
      plotted++;
      marker = L.circleMarker(coords, {
        radius: 7,
        color: "#ffffff",
        weight: 1.5,
        fillColor: "#d41530",
        fillOpacity: 0.92,
      }).addTo(leafletMap);
      // autoPan off so sweeping the cursor across dots doesn't drag the map
      marker.bindPopup(buildMapPopupHtml(church), { maxWidth: 260, autoPan: false });
      marker.on("click", () => highlightItem(i, true));

      // Hover: grow the dot and show its info right away. The popup stays
      // open on mouseout (so its links remain clickable) and Leaflet
      // auto-closes it when another dot's popup opens.
      marker.on("mouseover", () => {
        marker.setRadius(10);
        marker.setStyle({ weight: 2 });
        marker.openPopup();
      });
      marker.on("mouseout", () => {
        marker.setRadius(7);
        marker.setStyle({ weight: 1.5 });
      });
    }

    const selectChurch = () => {
      highlightItem(i, false);
      if (marker) {
        leafletMap.setView(marker.getLatLng(), Math.max(leafletMap.getZoom(), 13));
        marker.openPopup();
      } else {
        // No coordinates on file — show full details instead
        openChurchModal(church, item);
      }
    };

    item.addEventListener("click", selectChurch);
    item.addEventListener("keydown", (e) => {
      if (e.key !== "Enter" && e.key !== " ") return;
      e.preventDefault();
      selectChurch();
    });

    items.push(item);
    markers.push(marker);
    listDiv.appendChild(item);
  });

  statusBar.textContent = plotted === churches.length
    ? `${churches.length} church${churches.length !== 1 ? "es" : ""} on the map`
    : `${plotted} of ${churches.length} churches on the map`;

  // The visitor's own location, marked with a black pin (after Near me)
  if (userLoc) {
    L.marker(userLoc, {
      icon: L.divIcon({
        html: YOU_PIN_SVG,
        className: "you-pin",
        iconSize: [28, 40],
        iconAnchor: [14, 40],   // the pin's tip sits on the exact spot
      }),
      keyboard: false,
      zIndexOffset: 1000,       // always above the church dots
    }).addTo(leafletMap).bindTooltip("Your location", { direction: "top", offset: [0, -38] });
  }

  // Frame all dots; fall back to the statewide view when nothing plotted
  const points = markers.filter(Boolean).map((m) => m.getLatLng());
  // Keep the visitor's pin in frame too — unless they're far outside the
  // plotted area (an out-of-state visitor would zoom the map out absurdly)
  if (userLoc && points.some((p) =>
    Math.abs(p.lat - userLoc[0]) < 2 && Math.abs(p.lng - userLoc[1]) < 2
  )) {
    points.push(L.latLng(userLoc[0], userLoc[1]));
  }
  if (points.length > 1) {
    leafletMap.fitBounds(L.latLngBounds(points).pad(0.06));
  } else if (points.length === 1) {
    leafletMap.setView(points[0], 12);
  }

  // The container was just inserted — let layout settle, then size the map
  requestAnimationFrame(() => leafletMap && leafletMap.invalidateSize());
}

